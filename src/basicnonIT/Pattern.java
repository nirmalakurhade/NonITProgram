package basicnonIT;

public class Pattern {
	public static void main(String[] args) {
		int i,j,r=6;
		for
			(i=0; i <=r; i++){
		
			for
		        (j=0 ; j <=i ; j++) {
					System.out.print("*");
			}//end
		
			System.out.println();	
			}
		for (int i1 = 0; i1 <= r; i1--) {
			for (int j1 = r-i1; j1 >1; j1--) {
				System.out.print(i);
			}
			System.out.println("*");
			}
	}
	}
	
